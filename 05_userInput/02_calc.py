# making a "very basic" calculator
num1 = int(input("Enter the number: "))
num2 = int(input("Enter the number: "))

sum = num1 + num2
difference = num1 - num2
product = num1 * num2
quotient = num1 / num2
print(f"sum: {sum}")
print(f"difference: {difference}")
print(f"product: {product}")
print(f"quotient: {quotient}")