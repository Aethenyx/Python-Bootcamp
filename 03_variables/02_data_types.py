# Python has several built-in data types:
#     Integers (int): whole numbers (e.g., 10, -6, etc)
#     Floats (float): Decimal numbers (e.g., 3.14, -0.11, etc).
#     Strings (str): Text data enclosed in quotes (e.g., "Hello", "Aethenyx").
#     Booleans(bool): Represents True or False
#     Lists: Ordered mutable collections (e.g., [1, 2, 3, 4, 5]).
#     Tuples: Ordered, immutable collections (e.g., (1, 2, 3, 4, 5)).
#     Sets: Unordered collections of unique elements (e.g., {1, 2, 3, 4, 5}).
#     Dictionaries: key-value pairs (e.g., {"name": "Aethenyx", "age": "13"})

age = 13
print(age)
print(type(age))

cgpa = 9.8
print(cgpa)
print(type(cgpa))

name = "Aethenyx"
print(name)
print(type(name))

is_completed = True # can also be False
print(is_completed)
print(type(is_completed))