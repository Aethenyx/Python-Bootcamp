# Python supports several built-in data types
#   Integer (int): Whole numbers (e.g., 10, -5).
#   Float (float): Decimal numbers (e.g., 3.14, -0.001).
#   String (str): Textual data (e.g., "Hello, World!").
#   Booleans (bool): Represents True or False values.
#   Lists (list): Ordered, mutable collections of items (e.g., [1, 2, 3]).
#   Tuples (tuple): Ordered, immutable collections of items (e.g., (1, 2, 3)).
#   Sets (set): Unordered collections of unique items (e.g., {1, 2, 3}).
#   Dictionaries (dict): Key-value pairs (e.g., {"name": "Alice", "age": 30}).

age = 3
print(age)
print(type(age))

name = "Aethenyx"
print(name)
print(type(name))

cgpa  = 8.7
print(cgpa)
print(type(cgpa))

is_complete = True # can also be False
print(is_complete)
print(type(is_complete))